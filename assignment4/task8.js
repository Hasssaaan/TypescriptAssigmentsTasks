"use strict";
let num = 10; // Starting with num as 10
// Using += adds the right operand to the variable num and assigns the result to num
num += 5; // num is now 15
// Using -= subtracts the right operand from the variable num and assigns the result to num
num -= 2; // num is now 13
// Using *= multiplies the variable num by the right operand and assigns the result to num
num *= 3; // num is now 39
// Using /= divides the variable num by the right operand and assigns the result to num
num /= 3; // num is now 13
console.log(num); // Output the final value of num
